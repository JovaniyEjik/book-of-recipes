let searchFn = () => {
  let recepsSearch = document.querySelectorAll('.rec');
  recepsSearch.forEach(el => el.remove());
  let allR = [];
   Object.keys(ourBase).forEach((el,i) =>{
         Object.keys(ourBase[el].dishes).forEach((dis,i) => {
          allR.push(ourBase[el].dishes[dis].name)
        })
    })
    let req = document.querySelector('.inpSearch').value;
    
      let answ = [];
      
      allR.forEach((rec,j) => {
        
        for(let i =0;i < rec.length;i++){
          if(rec[i] == req[0]){
            
            for(let z = i; z < i+req.length;z++){

              if(rec[z] != req[z]){
                
                break;
              }
              else if(z == i+req.length-1){
                
                answ.push(rec);
              }
            }
          }
        }
      })
    let uu = [-1];
    if(answ.length != 0){
      answ.forEach((el,i)=>{
      for(let j =0;j < allR.length;j++){
        if(el == allR[j]){
          let y = uu.length;
          for(let z =0;z < uu.length;z++){
            if(j == uu[z]){
              break;
            }
            else if(z == uu.length-1){
              
              if(i < 12){
                uu.unshift(j);
              document.querySelector('.recepts').insertAdjacentHTML('beforeend',`<div class="rec" onclick = "recPer(${j})"><p>${el}</p></div>`)
              break;
              }
            }
          }
          if(uu.length>y){
            break;
          }
        }
      }
   
      })
    }
    else{
      document.querySelector('.recepts').insertAdjacentHTML('beforeend',`<div class="rec" ><p>Ничего не нашлось...</p></div>`)
    }
   
}

let recPer = (i) =>{
  
  if(numPage < i+3){
    
    let r = numPage+1;
    let time = setInterval(()=>{
      if(r < i+4){
        
        $("#flipbook").turn('page',r)
        r++;
      }
      else{
        
        clearInterval(time);
      }
    },150)
  }
  
  return numPage = i+3;
}
let searchMove = () => {
    numPage % 2 == 0 ? numPage+=0 : numPage--;
    let r = numPage;
    let time = setInterval(()=>{
      if(r != 0){
        
        $("#flipbook").turn('page',r)
        r-=2;
      }
      else{
        
        clearInterval(time);
      }
    },150)
    return numPage = 2;
}


let transitionP = (a) => {
  let keys = Object.keys(ourBase);
    keys.forEach((el,i) =>{
      
        let dish = Object.keys(ourBase[el].dishes);
        
        dish.forEach((dis,i) => {
          a--;
          if(a == 0){
            localStorage.setItem('persone', el);
            localStorage.setItem('recept', dis);
            window.location.href = '../rec/index.html';
          }

        })
      
      })
}
















